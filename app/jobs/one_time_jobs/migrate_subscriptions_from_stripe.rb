module OneTimeJobs
  class MigrateSubscriptionsFromStripe
    def run(api_key:, hq_event_id: 183)
      subscriptions = StripeService::Subscription.list(
        {
          limit: 100,
          status: "active",
          expand: ["data.customer", "data.customer.default_source"]
        },
        { api_key: api_key }
      )

      event = Event.find(hq_event_id)

      subscriptions_to_create = []

      subscriptions.each do |subscription|
        next if subscription.items.data[0].price.unit_amount == 0

        subscriptions_to_create << {
          customer: {
            name: subscription.customer.name || subscription.customer.email,
            email: subscription.customer.email,
            id: subscription.customer.id
          },
          amount: subscription.items.data[0].price.unit_amount,
          default_payment_method: COPY_CSV.find { |row| row[1] == subscription.customer.default_source.id }[3],
          default_payment_method_last4: subscription.customer.default_source.last4,
          billing_cycle_anchor: subscription.current_period_end,
          backdate_start_date: subscription.start_date,
          old_subscription_id: subscription.id,
        }
      end

      puts(subscriptions_to_create.map do |s|
        "#{s[:old_subscription_id]}: #{s[:customer][:email]} - $#{s[:amount] / 100}/mo - ••••#{s[:default_payment_method_last4]} (#{s[:default_payment_method]}) - #{Time.at(s[:billing_cycle_anchor]).strftime("%m/%d/%Y")})}"
      end.join("\n"))
      puts "#{subscriptions_to_create.size} subscriptions"

      puts "\ntype the ID of a subscription to migrate, 'y' to migrate all, or 'n' to cancel:"

      text = gets
      return if text == "n\n"

      if text != "y\n"
        subscriptions_to_create = subscriptions_to_create.select { |s| s[:old_subscription_id] == text.strip }
      end

      subscriptions_to_create.each do |subscription|
        puts "subscribing #{subscription[:customer][:email]}..."

        price = StripeService::Price.create(
          currency: "usd",
          unit_amount: subscription[:amount],
          recurring: { interval: "month" },
          product_data: { name: "Recurring donation to #{event.name}", statement_descriptor: StripeService::StatementDescriptor.format(event.name) }
        )

        stripe_subscription = Stripe::Subscription.create(
          customer: subscription[:customer][:id],
          items: [
            { price: price.id }
          ],
          billing_cycle_anchor: subscription[:billing_cycle_anchor],
          backdate_start_date: subscription[:backdate_start_date],
          proration_behavior: "none",
          default_payment_method: subscription[:default_payment_method],
        )

        recurring_donation = RecurringDonation.new(
          name: subscription[:customer][:name],
          email: subscription[:customer][:email],
          amount: subscription[:amount],
          event_id: hq_event_id,
          stripe_subscription_id: stripe_subscription.id,
          migrated_from_legacy_stripe_account: true
        )
        recurring_donation.sync_with_stripe_subscription!

        recurring_donation.save!

        # cancel their old subscription
        StripeService::Subscription.cancel({ id: subscription[:old_subscription_id] }, { api_key: api_key })
      end

      nil
    end

  end
